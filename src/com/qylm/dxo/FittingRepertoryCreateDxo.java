package com.qylm.dxo;

import com.qylm.dto.fitting.FittingRepertoryCreateDto;
import com.qylm.entity.FittingRepertory;

public final class FittingRepertoryCreateDxo {

	public static void dtoToEntity(FittingRepertoryCreateDto dto, FittingRepertory entity) {
		entity.setFittingInfo(dto.getFittingInfo());
		entity.setRepertorySum(dto.getRepertorySum());
		entity.setCreater(dto.getCreater());
		entity.setBelongingUser(dto.getBelongingUser());
	}

	public static void entityToDto(FittingRepertory entity, FittingRepertoryCreateDto dto) {
		dto.setFittingInfo(entity.getFittingInfo());
		dto.setRepertorySum(entity.getRepertorySum());
		dto.setCreater(entity.getCreater());
		dto.setBelongingUser(entity.getBelongingUser());
	}

}
