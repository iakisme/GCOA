package com.qylm.dxo;

import com.qylm.dto.vehicle.VehicleServicingCreateDto;
import com.qylm.entity.VehicleServicing;

public final class VehicleServicingCreateDxo {

	public static void dtoToEntity(VehicleServicingCreateDto dto, VehicleServicing entity) {
		entity.setVehicleInfo(dto.getVehicleInfo());
		entity.setEmployee(dto.getEmployee());
		entity.setBreakdown(dto.getBreakdown());
		entity.setCost(dto.getCost());
		entity.setRemark(dto.getRemark());
		entity.setState(dto.getState());
		entity.setServicingDate(dto.getServicingDate());
		entity.setCreater(dto.getCreater());
		entity.setBelongingUser(dto.getBelongingUser());
	}

	public static void entityToDto(VehicleServicing entity, VehicleServicingCreateDto dto) {
		dto.setVehicleInfo(entity.getVehicleInfo());
		dto.setEmployee(entity.getEmployee());
		dto.setBreakdown(entity.getBreakdown());
		dto.setCost(entity.getCost());
		dto.setRemark(entity.getRemark());
		dto.setState(entity.getState());
		dto.setServicingDate(entity.getServicingDate());
		dto.setCreater(entity.getCreater());
		dto.setBelongingUser(entity.getBelongingUser());
	}

}
