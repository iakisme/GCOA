package com.qylm.dto.report;

import java.io.Serializable;
import java.util.Date;

/**
 * 保存生产报表统计管理画面需要的值
 * @author smj
 */
public class ProductionStatisticsManageDto implements Serializable {

	/**
	 * serialVersionUID
	 */
	private static final long serialVersionUID = 2070107954373570111L;
	
	/**
	 * 客户
	 */
	private String customerName;
	
	/**
	 * 车辆编号
	 */
	private String number;
	
	/**
	 * 工作地址
	 */
	private String workAddress;
	
	/**
	 * 项目状态
	 * 1：未生效，（显示红色#FF0000）
	 * 2：工作安排中，（显示黄色#FFD700）
	 * 3：工作已安排，（显示绿色#00FF00）
	 * 4：工作暂停，（显示暗红色#FF4040）
	 * 5：工作完结，（显示蓝色#0000FF）
	 */
	private String type;
	
	/**
	 * 月份
	 */
	private Date month;
	
	/**
	 * 年月（列表显示用）
	 */
	private String yearMonth;
	
	/**
	 * 日期
	 */
	private Date beginDate;
	
	/**
	 * @return the customerName
	 */
	public String getCustomerName() {
		return customerName;
	}

	/**
	 * @param customerName the customerName to set
	 */
	public void setCustomerName(String customerName) {
		this.customerName = customerName;
	}

	/**
	 * @return the number
	 */
	public String getNumber() {
		return number;
	}

	/**
	 * @param number the number to set
	 */
	public void setNumber(String number) {
		this.number = number;
	}

	/**
	 * @return the workAddress
	 */
	public String getWorkAddress() {
		return workAddress;
	}

	/**
	 * @param workAddress the workAddress to set
	 */
	public void setWorkAddress(String workAddress) {
		this.workAddress = workAddress;
	}

	/**
	 * @return the type
	 */
	public String getType() {
		return type;
	}

	/**
	 * @param type the type to set
	 */
	public void setType(String type) {
		this.type = type;
	}

	/**
	 * @return the month
	 */
	public Date getMonth() {
		return month;
	}

	/**
	 * @param month the month to set
	 */
	public void setMonth(Date month) {
		this.month = month;
	}

	/**
	 * @return the yearMonth
	 */
	public String getYearMonth() {
		return yearMonth;
	}

	/**
	 * @param yearMonth the yearMonth to set
	 */
	public void setYearMonth(String yearMonth) {
		this.yearMonth = yearMonth;
	}

	/**
	 * @return the beginDate
	 */
	public Date getBeginDate() {
		return beginDate;
	}

	/**
	 * @param beginDate the beginDate to set
	 */
	public void setBeginDate(Date beginDate) {
		this.beginDate = beginDate;
	}

}
