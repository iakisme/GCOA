package com.qylm.dto.fitting;

import java.io.Serializable;
import java.math.BigDecimal;
import java.util.List;

import com.qylm.bean.returner.Returner;
import com.qylm.entity.FittingInfo;
import com.qylm.entity.FittingRepertory;
import com.qylm.entity.User;

/**
 * 保存配件库存信息管理画面需要的值
 * @author smj
 */
public class FittingRepertoryCreateDto implements Serializable {
	
	/**
	 * serialVersionUID
	 */
	private static final long serialVersionUID = 9076082133582214091L;

	/**
	 * 配件信息
	 */
	private FittingInfo fittingInfo;

	/**
	 * 库存现数量
	 */
	private BigDecimal repertorySum;
	
	/**
	 * 创建配件
	 */
	private User creater;
	
	/**
	 * 归属公司
	 */
	private User belongingUser;
	
	/**
	 * 修改传值
	 */
	private FittingRepertory transferFittingRepertory;
	
	/**
	 * 返回共通方法
	 */
	private Returner<?, ?, ?> returner;
	
	/**
	 * 配件名称（搜索条件）
	 */
	private String fittingName;
	
	/**
	 * 配件信息集合
	 */
	private List<FittingInfo> fittingInfoList;

	/**
	 * @return the fittingInfo
	 */
	public FittingInfo getFittingInfo() {
		return fittingInfo;
	}

	/**
	 * @param fittingInfo the fittingInfo to set
	 */
	public void setFittingInfo(FittingInfo fittingInfo) {
		this.fittingInfo = fittingInfo;
	}

	/**
	 * @return the repertorySum
	 */
	public BigDecimal getRepertorySum() {
		return repertorySum;
	}

	/**
	 * @param repertorySum the repertorySum to set
	 */
	public void setRepertorySum(BigDecimal repertorySum) {
		this.repertorySum = repertorySum;
	}

	/**
	 * @return the creater
	 */
	public User getCreater() {
		return creater;
	}

	/**
	 * @param creater the creater to set
	 */
	public void setCreater(User creater) {
		this.creater = creater;
	}

	/**
	 * @return the belongingUser
	 */
	public User getBelongingUser() {
		return belongingUser;
	}

	/**
	 * @param belongingUser the belongingUser to set
	 */
	public void setBelongingUser(User belongingUser) {
		this.belongingUser = belongingUser;
	}

	/**
	 * @return the transferFittingRepertory
	 */
	public FittingRepertory getTransferFittingRepertory() {
		return transferFittingRepertory;
	}

	/**
	 * @param transferFittingRepertory the transferFittingRepertory to set
	 */
	public void setTransferFittingRepertory(
			FittingRepertory transferFittingRepertory) {
		this.transferFittingRepertory = transferFittingRepertory;
	}

	/**
	 * @return the returner
	 */
	public Returner<?, ?, ?> getReturner() {
		return returner;
	}

	/**
	 * @param returner the returner to set
	 */
	public void setReturner(Returner<?, ?, ?> returner) {
		this.returner = returner;
	}

	/**
	 * @return the fittingName
	 */
	public String getFittingName() {
		return fittingName;
	}

	/**
	 * @param fittingName the fittingName to set
	 */
	public void setFittingName(String fittingName) {
		this.fittingName = fittingName;
	}

	/**
	 * @return the fittingInfoList
	 */
	public List<FittingInfo> getFittingInfoList() {
		return fittingInfoList;
	}

	/**
	 * @param fittingInfoList the fittingInfoList to set
	 */
	public void setFittingInfoList(List<FittingInfo> fittingInfoList) {
		this.fittingInfoList = fittingInfoList;
	}

}
